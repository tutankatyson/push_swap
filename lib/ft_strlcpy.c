#include "libft.h"

size_t	ft_strlen(const char *s);

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	int				srcsize;
	unsigned int	i;

	srcsize = ft_strlen(src);
	i = 0;
	if (dstsize == 0)
	{
		return (srcsize);
	}
	while (src[i] != '\0' && i < (dstsize - 1))
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (srcsize);
}
